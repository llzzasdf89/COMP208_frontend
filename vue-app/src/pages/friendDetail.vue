<template>
  <v-content>
    <transition>
  <router-view></router-view>
</transition>
    <v-container app class="fill-height d-flex align-content-start fluid align-center " v-if="$route.name!='Chat'" style="background:rgb(243,245,250)">
      <friendItem></friendItem>
      <tags :isDisabled="true" :textColor="'white'"></tags>
      <userReport class='mx-5'></userReport>
      <v-footer app>
        <v-row dense>
          <v-col cols="6" class="d-flex flex-column align-center">
            <v-btn icon @click='toChat'>
              <v-icon>mdi-message</v-icon>
            </v-btn>
            <span class="subtitle-2">chat</span>
          </v-col>
          <v-col cols="6" class="d-flex flex-column align-center">
            <v-btn icon @click='focus'>
              <v-icon v-if='!isFocus'>mdi-plus-box-outline</v-icon>
              <v-icon v-else>mdi-check</v-icon>
            </v-btn>
            <span class="subtitle-2">{{focusText}}</span>
          </v-col>
        </v-row>
      </v-footer>
    </v-container>
  </v-content>
</template>
<script>
import friendItem from '@/components/friendItem'
import tags from '@/components/tags'
import userReport from '@/components/userReport'
export default {
  data: function () {
    return {
      isFocus: false,
      focusText: 'focus'
    }
  },
  methods: {
    focus: function () {
      const isFocus = this.$data.isFocus
      this.$data.isFocus = !isFocus
      if (isFocus) this.$data.focusText = 'focus'
      else this.$data.focusText = 'focused'
    },
    toChat: function () {
      this.$router.push({ name: 'Chat' })
    }
  },
  components: {
    friendItem,
    tags,
    userReport
  }
}
</script>
